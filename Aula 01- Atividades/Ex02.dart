import 'package:flutter/material.dart';

/* 2. Calcular e exibir a média aritmética de quatro 
* valores quaisquer que serão digitados.
*/
void main() {
  double v1 = 5;
  double v2 = 25;
  double v3 = 10;
  double v4 = 50;

  double media = (v1 + v2 + v3 + v4) / 4;

  print("A média aritmética é $media");
}